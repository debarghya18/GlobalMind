def get_translation(language_code: str, key: str) -> str:
    """
    Get translation for a given language and key
    
    Args:
        language_code: Language code (e.g., 'en', 'es')
        key: Translation key
        
    Returns:
        str: Translated text
    """
    return TRANSLATIONS.get(language_code, TRANSLATIONS['en']).get(key, key)

def get_language_name(language_code: str) -> str:
    """
    Get display name for a language code
    
    Args:
        language_code: Language code
        
    Returns:
        str: Language display name
    """
    return LANGUAGE_NAMES.get(language_code, language_code.upper())

def get_supported_languages() -> dict:
    """
    Get all supported languages
    
    Returns:
        dict: Language code to display name mapping
    """
    return LANGUAGE_NAMES
